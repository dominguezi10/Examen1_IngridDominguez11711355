            t_nombreP.setText("");
            t_edadP.setText("");
            rb_fP.setSelected(true);
            rb_mP.setSelected(false);
            t_estadoCivilP.setText("");
            t_ocupacion.setText("");
            t_hE.setText("");
            t_hS.setText("");
            c_fechaP.setDate(new Date());
            t_sueldoP.setText("");
            t_contraseñaP.setText("");

